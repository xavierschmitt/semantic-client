from systems import materials_commons


def addition(arg1, arg2):
    """ Call addition on MC
    
    :param arg1: 
    :param arg2: 
    :return: 
    """
    materials_commons.add(arg1, arg2)


def subtraction(arg1, arg2):
    """ Call subtraction on MC
    
    :param arg1: 
    :param arg2: 
    :return: 
    """
    materials_commons.sub(arg1, arg2)
