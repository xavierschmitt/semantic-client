from systems import mdcs


def addition(arg1, arg2):
    """ Call addition on MDCS

    :param arg1:
    :param arg2:
    :return:
    """
    mdcs.add(arg1, arg2)


def subtraction(arg1, arg2):
    """ Call subtraction on MDCS

    :param arg1:
    :param arg2:
    :return:
    """
    mdcs.sub(arg1, arg2)
