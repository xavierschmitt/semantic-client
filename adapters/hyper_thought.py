from systems import hyper_thought


def addition(arg1, arg2):
    """ Call addition on HT

    :param arg1:
    :param arg2:
    :return:
    """
    hyper_thought.add(arg1, arg2)


def subtraction(arg1, arg2):
    """ Call subtraction on HT

    :param arg1:
    :param arg2:
    :return:
    """
    hyper_thought.sub(arg1, arg2)
