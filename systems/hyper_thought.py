def add(input_1, input_2):
    """ Addition integers

    Args:
        input_1: int
        input_2: int

    Returns:

    """
    return input_1 + input_2


def sub(input_1, input_2):
    """ Substract integers

    Args:
        input_1: int
        input_2: int

    Returns:

    """
    return input_1 - input_2
